#include "minishell.h"

void	sig_int(void)
{
	if (g_kost == 0)
	{
		fd_putstr("\n", 1);
		rl_on_new_line();
		rl_replace_line("", 0);
		rl_redisplay();
	}
	else
		fd_putstr("\n", 1);
	g_kost = 0;
}
